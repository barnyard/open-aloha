package com.bt.aloha.batchtest;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.springframework.dao.DataAccessException;
import org.springframework.jdbc.core.JdbcTemplate;

public class MaintenanceDao {
    private JdbcTemplate jdbcTemplate;
    private static Log log = LogFactory.getLog(MaintenanceDao.class);

	public JdbcTemplate getJdbcTemplate() {
		return jdbcTemplate;
	}

	public void setJdbcTemplate(JdbcTemplate jdbcTemplate) {
		this.jdbcTemplate = jdbcTemplate;
	}

	public void truncateAllTables() {
		truncateTable("callinfo");
		truncateTable("conferenceinfo");
		truncateTable("stateinfo");
	}

	private void truncateTable(String tablename){
		try{
			jdbcTemplate.execute("truncate "+tablename+";");
		}
		catch(DataAccessException e){
			log.warn("Unable to truncate " + tablename, e);
		}
	}

}
